def greet_user(name):
    return f"Hello, {name}! Welcome to Python programming."

def calculate_sum(a, b):
    return a + b

def find_maximum(numbers):
    return max(numbers)

def reverse_string(s):
    return s[::-1]

def get_user_info(user_id, age, status):
    return {
        "user_id": user_id, 
        "age": age, 
        "status": status
    }